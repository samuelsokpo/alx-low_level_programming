#include "3-calc.h"

/**
  * op_add - function that add two numbers
  * @a: number 1
  * @b: number 2
  * Return: result
  */

int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - function that rest two numbers
  * @a: number 1
  * @b: number 2
  * Return: result
  */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - function that multiplicate
  * @a: number 1
  * @b: number 2
  * Return: result
  */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - function that divide two numbers
  * @a: number 1
  * @b: number 2
  * Return: result
  */

int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - function that return the mod of two numbers
  * @a: number 1
  * @b: number 2
  * Return: result
  */

int op_mod(int a, int b)
{
	return (a % b);
}

